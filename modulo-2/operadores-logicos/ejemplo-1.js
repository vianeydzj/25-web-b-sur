//Operador de doble igualdad 
//compara que dos valores sean iguales 

if (4 == '4'){
    console.log('Doble igualdad. 4 numero es igual a 4 string');
}

//operador de triple igualdad 
//compara que dos valores sean iguales y que sean del mismo tipo de dato

if (4 === '4'){
    console.log('4 numero es igual a 4 string');
}
else {
    console.log('Triple igualdad. 4 numero no es igual a 4 string');
}
